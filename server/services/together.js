function dedupeExact(questions) {
  const seen = new Set();
  const out = [];
  for (const q of questions) {
    const key = (q.question || "").toLowerCase().trim();
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(q);
  }
  return out;
}

